public class RGB {
    private int R, G, B;

    public RGB(){

    }

    //set methods
    public void setR(int R){
        this.R = R;
    }
    public void setG(int G){
        this.G = G;
    }
    public void setB(int B){
        this.B = B;
    }

    //get methods
    public int getR(){
        return this.R;
    }

    public int getG(){
        return this.G;
    }

    public int getB(){
        return this.B;
    }





}
